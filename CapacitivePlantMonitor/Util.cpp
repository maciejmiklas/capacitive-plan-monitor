#include "Util.h"

static uint32_t cycleMs;

void util_setup() {
  util_cycle();
}

void util_cycle() {
  cycleMs = millis() * CP_CLOCK_DIV_INT;
}

uint32_t util_ms() {
  return cycleMs;
}